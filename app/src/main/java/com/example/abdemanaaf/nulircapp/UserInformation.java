package com.example.abdemanaaf.nulircapp;

public class UserInformation {

    private String name;
    private String email;

    UserInformation() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
